#Simple calculator

def addition():
    a = int(input('Type first number '))
    b = int(input('Type second number '))
    print(a + b)

def subtraction():
    a = int(input('Type first number '))
    b = int(input('Type second number '))
    print(a - b)

def multiplication():
    a = int(input('Type first number '))
    b = int(input('Type second number '))
    print(a * b)

def division():
    a = int(input('Type first number '))
    b = int(input('Type second number '))
    print(a / b)
